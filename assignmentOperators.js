// +=, -=, *=, /= , %= 

// Addition assignment Operators

var num1 = 20;
console.log(num1)
num1 += 15;
console.log(num1)


// Subtraction Assignment operator

var num2 = 10;
console.log(num2)
num2 -= 5;
console.log(num2)


// Multiplication Assignment operator

var num3 = 20;
console.log(num3)
num3 *= 5;
console.log(num3)

// Division 
var num4 =  12;
console.log(num4)
num4 /= 3;
console.log(num4)


var num5 = 15;
console.log(num5)
num5 %= 5;
console.log(num5)